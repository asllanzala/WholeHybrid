public class ProductChildItem {




    private String productChildName;
    private String productChildPrice;
    private String productChildNumber;


    public String getProductChildName() {
        return productChildName;
    }

    public String getProductChildPrice() {
        return productChildPrice;
    }

    public String getProductChildNumber() {
        return productChildNumber;
    }

    public void setProductChildName(String productChildName) {
        this.productChildName = productChildName;
    }

    public void setProductChildPrice(String productChildPrice) {
        this.productChildPrice = productChildPrice;
    }

    public void setProductChildNumber(String productChildNumber) {
        this.productChildNumber = productChildNumber;
    }
}
